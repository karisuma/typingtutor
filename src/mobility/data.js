function buildGraph(data) {
  const graph = new Map();
  const addEdge = (a, b) => {
    if (!graph.has(a)) graph.set(a, []);
    if (!graph.has(b)) graph.set(b, []);
    if (!graph.get(a).includes(b)) graph.get(a).push(b);
    if (!graph.get(b).includes(a)) graph.get(b).push(a);
  };
  Object.values(data.lines).forEach((line) => {
    const list = line.stations;
    for (let i = 0; i < list.length - 1; i++) {
      if (data.stations[list[i]] && data.stations[list[i + 1]]) addEdge(list[i], list[i + 1]);
    }
    if (line.circular && list.length > 2) addEdge(list.at(-1), list[0]);
  });
  Object.keys(data.stations).forEach((id) => {
    if (!graph.has(id)) graph.set(id, []);
  });
  return graph;
}

