// 그래프 빌드: 역 ID -> 인접 역 ID 배열 (edge weight = 1)
function buildGraph() {
  const stations = SUBWAY_DATA.stations;
  const lines = SUBWAY_DATA.lines;
  const adj = new Map();

  const addEdge = (a, b) => {
    if (!adj.has(a)) adj.set(a, []);
    if (!adj.has(b)) adj.set(b, []);
    if (!adj.get(a).includes(b)) adj.get(a).push(b);
    if (!adj.get(b).includes(a)) adj.get(b).push(a);
  };

  Object.values(lines).forEach((line) => {
    const list = line.seoulOnly || line.stations;
    for (let i = 0; i < list.length - 1; i++) {
      const a = list[i];
      const b = list[i + 1];
      if (stations[a] && stations[b]) addEdge(a, b);
    }
  });

  return adj;
}

